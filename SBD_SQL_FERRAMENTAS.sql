CREATE TABLE CARRINHO (
    COD_FERRAMENTA CHAR,
    ID_CARRINHO CHAR PRIMARY KEY,
    ID_FERRAMENTA CHAR,
    FK_FUNCIONARIO_ID_FUNCIONARIO CHAR
);

CREATE TABLE FUNCIONARIO (
    ID_CARRINHO CHAR,
    ID_FUNCIONARIO CHAR PRIMARY KEY
);
 
